package net.epiclanka.training.model;

public class CustomerBillHistory {

	private int paymentcount;
	private String encodedCustomerHistory;
	
	
	public int getPaymentcount() {
		return paymentcount;
	}
	public void setPaymentcount(int paymentcount) {
		this.paymentcount = paymentcount;
	}
	public String getEncodedCustomerHistory() {
		return encodedCustomerHistory;
	}
	public void setEncodedCustomerHistory(String encodedCustomerHistory) {
		this.encodedCustomerHistory = encodedCustomerHistory;
	}
	
	
}
